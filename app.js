function newFunction() {
    var x = prompt("enter your First value: ")
    var y = prompt("enter your Second value: ")
    var oper = prompt("enter your Operator: ")

    if (oper === "+") {
        console.log((+x) + (+y))

    }
    else if (oper === "-") {

        console.log((+x) - (+y))
    }
    else if (oper === "*") {

        console.log((+x) * (+y))
    }
    else if (oper === "/") {

        console.log((+x) / (+y))
    }
    else if (oper === "%") {

        console.log((+x) % (+y))
    }
}
